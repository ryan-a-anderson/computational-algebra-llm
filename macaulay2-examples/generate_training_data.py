def parse_args():
    p = argparse.ArgumentParser(description="Generate M2 code-generation training data")
    p.add_argument(
        "--variants",
        type=int,
        default=2,
        help="Question phrasings per template via Nemotron (default: 2)",
    )
    p.add_argument(
        "--no-tinker",
        action="store_true",
        help="Skip Tinker annotation and use template questions directly",
    )
    p.add_argument(
        "--dry-run",
        action="store_true",
        help="Show stats without generating or calling Tinker",
    )
    return p.parse_args()


async def main_async(args):
    print("Generating template examples...")
    templates = generate_all_templates()

    if args.dry_run:
        print(f"\n-- Dry run --")
        print(f"Templates: {len(templates)}")
        print(f"Variants per template: {args.variants if not args.no_tinker else 1}")
        est = len(templates) if args.no_tinker else len(templates) * args.variants
        print(f"Estimated output records: {est}")
        print(f"\nSample template:")
        q, code, cat = templates[0]
        print(f"  Category: {cat}")
        print(f"  Question: {q}")
        print(f"  Code:\n    {code.replace(chr(10), chr(10) + '    ')}")
        return

    if args.no_tinker:
        print("Building records without Tinker annotation...")
        records = build_records_no_tinker(templates)
    else:
        records = await annotate_with_tinker(templates, args.variants)

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_PATH, "w") as f:
        json.dump(records, f, indent=2)
    print(f"\nSaved {len(records)} training records to {OUTPUT_PATH}")


def main():
    args = parse_args()
    asyncio.run(main_async(args))


if __name__ == "__main__":
    main()
