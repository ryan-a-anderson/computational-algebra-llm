VARS_SMALL = [["x", "y"], ["x", "y", "z"], ["a", "b", "c"], ["s", "t"], ["u", "v", "w"]]
VARS_SINGLE = ["x", "t", "u"]
FIELDS = ["QQ", "ZZ/2", "ZZ/3", "ZZ/5", "ZZ/7", "ZZ/11", "GF(4)", "GF(8)"]
FIELDS_MAIN = ["QQ", "QQ", "QQ", "ZZ/2", "ZZ/5"]  # weighted toward QQ


def rfield():
    return random.choice(FIELDS_MAIN)


def rvars():
    return random.choice(VARS_SMALL)


def rvar():
    return random.choice(VARS_SINGLE)


def rpoly(variables, max_degree=3, max_terms=3, coeff_range=5):
    """Return a random polynomial string in the given variables."""
    terms = []
    n_terms = random.randint(1, max_terms)
    for _ in range(n_terms):
        coeff = random.randint(-coeff_range, coeff_range)
        if coeff == 0:
            coeff = 1
        parts = []
        all_zero = True
        for v in variables:
            exp = random.randint(0, max_degree)
            if exp > 0:
                all_zero = False
                parts.append(f"{v}^{exp}" if exp > 1 else v)
        mono = "*".join(parts) if parts else "1"
        if all_zero:
            # constant term
            terms.append(str(coeff))
        elif coeff == 1:
            terms.append(mono)
        elif coeff == -1:
            terms.append(f"-{mono}")
        else:
            terms.append(f"{coeff}*{mono}")
    # deduplicate by rebuilding
    return " + ".join(terms).replace("+ -", "- ")


def rpolys(variables, n, **kwargs):
    return [rpoly(variables, **kwargs) for _ in range(n)]


def ring_decl(field, variables):
    return f"R = {field}[{', '.join(variables)}]"


def ideal_decl(polys):
    return f"I = ideal({', '.join(polys)})"


